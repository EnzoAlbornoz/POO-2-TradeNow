    private void defineTableData(String[][] data,String[] header){
        lastUpdate = data[0];
        tableHeader = header;
        for(int i = 1;i < data.length;i++){
            tableData[i] = data[i];
        }
    }
    
    private String generateLabels(){
        String r = "";
        for(int i = 0;i < tableHeader.length;i++){
            r += tableHeader[i] + " : " + tableData[0][i] + "\n";
        }
        return r;
    }
    
